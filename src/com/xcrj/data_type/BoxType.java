package com.xcrj.data_type;

public class BoxType {
    public static void main(String[] args) {
        //整数
        Byte aByte=new Byte((byte)10);
        Short aShort=new Short((short)10);
        Integer aInteger=new Integer(10);
        Long aLong=new Long(10L);

        //浮点数
        Float aFloat=new Float(10.1f);
        Double aDouble=new Double(10.1);

        //布尔
        Boolean aBoolean=new Boolean(true);

        //字符
        Character aCharacter=new Character('c');
    }
}
